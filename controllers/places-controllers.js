// <!---- get place by id ---->
const getPlaceById = async (req, res, next) => {
  const placeId = req.params.pid; // { pid: 'p1' }

  // <!---- get place by id try catch to check data is available or not in database---->
  let place;
  try {
    place = await Place.findById(placeId);
  } catch (err) {
    const error = new HttpError(
      "something went wrong  could not fine data",
      500
    );
    return next(error);
  }

  // <!---- get place by id try catch to check data is available or not ---->
  if (!place) {
    const error = new HttpError(
      "Could not find a place for the provided id.",
      404
    );
    return next(error);
  }

  res.json({ place: place.toObject({ getters: true }) }); // => { place } => { place: place }
};

